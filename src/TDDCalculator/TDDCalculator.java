package TDDCalculator;


public class TDDCalculator {

    public int add(int a, int b, String operator){
        return a + b;
    }
    
    public int subtract(int a, int b, String operator){
        return a - b;
    }
    
    public int multiply(int a, int b, String operator){
        return a * b;
    }
    
    public int divide(int a, int b, String operator){
        return a / b;
    }
    
}
